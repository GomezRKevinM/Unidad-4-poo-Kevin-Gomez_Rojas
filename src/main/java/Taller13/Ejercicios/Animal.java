package Taller13.Ejercicios;

public abstract class Animal {
    public void comer() {
        System.out.println("El animal está comiendo.");
    }

    /**
     * En Java, no es posible definir métodos predeterminados (default) en una clase abstracta.
     * Los métodos default solo pueden usarse en interfaces, no en clases abstractas.
     */
}
